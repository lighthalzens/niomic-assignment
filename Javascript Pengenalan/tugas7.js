function loopingSatu() {
    var tb = [165, 164, 175, 153, 175, 156, 179, 181, 167, 178];
    for (let x of tb) {
        console.log(x);
    }
}

function loopingDua() {
    var tb = [165, 164, 175, 153, 175, 156, 179, 181, 167, 178];
    for (i = 0; i < tb.length; i++) {
        console.log(tb[i]);
    }
}

loopingSatu();
loopingDua();